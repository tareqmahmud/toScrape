def str_to_int(str_data):
    str_data = str(str_data).lower().strip()

    if str_data == "zero":
        return 0
    elif str_data == "one":
        return 1
    elif str_data == "two":
        return 2
    elif str_data == "three":
        return 3
    elif str_data == "four":
        return 4
    elif str_data == "five":
        return 5
